
package tp_pbo_5;

public class While {
    public static void main(String[] args) {
        
        int a = 1;
        System.out.println("Perulangan while (i++):");
        while (a <= 5) {
            System.out.print(a + " ");
            a++;
        } 
   
        int b = 1;
        System.out.println("Perulangan while (++i):");
        while (b <= 5) {
            System.out.print(b + " ");
            ++b;
        } 
    
    }
}
